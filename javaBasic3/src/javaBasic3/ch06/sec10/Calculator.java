package javaBasic3.ch06.sec10;

public class Calculator {
	
	static double pi = 3.1419;
	
	static int plus(int x, int y)
	{
		return x+y;
	}
	
	static int minus(int x, int y)
	{
		return x-y;
	}
	
	static int mul(int x , int y)
	{
		return x*y;
	}
	
	static int div(int x, int y)
	{
		return x/y;
	}

}
